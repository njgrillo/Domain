    public static AccomplishmentTypes Find(int id)
        => All.SingleOrDefault(AccomplishmentTypes => AccomplishmentTypes.Id == id);

    public static AccomplishmentTypes[] GetAll(params Sports[] sports)
    {
        if (sports.IsNullOrEmpty())
            return All;

        var AccomplishmentTypess = new List<AccomplishmentTypes>();

        if (sports.Any(sport => sport == Sports.Baseball))
            AccomplishmentTypess.AddRange(Baseball);

        if (sports.Any(sport => sport == Sports.Basketball))
            AccomplishmentTypess.AddRange(Basketball);

        if (sports.Any(sport => sport == Sports.Football))
            AccomplishmentTypess.AddRange(Football);

        return AccomplishmentTypess.OrderBy(AccomplishmentTypes => AccomplishmentTypes.Name)
                                  .ToArray();
    }

    public static AccomplishmentTypes[] GetAll(SportLeagueLevels sportLeagueLevel)
    {
        if (sportLeagueLevel == SportLeagueLevels.MajorLeagueBaseball)
            return Baseball;

        if (sportLeagueLevel == SportLeagueLevels.NationalBasketballAssociation)
            return Basketball;

        if (sportLeagueLevel == SportLeagueLevels.NationalFootballLeague)
            return Football;

        return All;
    }
