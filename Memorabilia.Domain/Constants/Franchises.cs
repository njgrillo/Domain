    private Franchises(int id, string name) 
        : base(id, name) { }

    public static Franchises Find(int id)
        => All.SingleOrDefault(Franchises => Franchises.Id == id);

    public static Franchises Find(string name)
        => All.SingleOrDefault(Franchises => Franchises.Name == name);

    public static Franchises[] GetAll(SportLeagueLevels sport)
    {
        if (sport == SportLeagueLevels.MajorLeagueBaseball)
            return Baseball;

        if (sport == SportLeagueLevels.NationalBasketballAssociation)
            return Basketball;

        if (sport == SportLeagueLevels.NationalFootballLeague)
            return Football;

        return All;
    }

    public static Franchises[] GetAll(params Sports[] sports)
    {
        if (sports.IsNullOrEmpty())
            return All;

        var Franchisess = new List<Franchises>();

        if (sports.Any(sport => sport == Sports.Baseball))
            Franchisess.AddRange(Baseball);

        if (sports.Any(sport => sport == Sports.Basketball))
            Franchisess.AddRange(Basketball);

        if (sports.Any(sport => sport == Sports.Football))
            Franchisess.AddRange(Football);

        return Franchisess.OrderBy(Franchises => Franchises.Name).ToArray();
    }

    public static Sports GetSport(int id)
    {
        var Franchises = Find(id);

        if (Baseball.Contains(Franchises))
            return Sports.Baseball;

        if (Basketball.Contains(Franchises))
            return Sports.Basketball;

        if (Football.Contains(Franchises))
            return Sports.Football;

        return null;
    }
