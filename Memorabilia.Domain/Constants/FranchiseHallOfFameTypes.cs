        Franchise = franchise;
    }

    public Franchises Franchise { get; }

    public static FranchiseHallOfFameTypes Find(int franchiseId)
        => All.SingleOrDefault(franchiseHallOfFameType => franchiseHallOfFameType.Franchise == Franchises.Find(franchiseId));

    public static FranchiseHallOfFameTypes Find(Franchises franchise)
        => All.SingleOrDefault(franchiseHallOfFameType => franchiseHallOfFameType.Franchise == franchise);

    public static FranchiseHallOfFameTypes[] GetAll(params Sports[] sports)
    {
        if (sports.IsNullOrEmpty())
            return All;

        var franchiseHallOfFameTypes = new List<FranchiseHallOfFameTypes>();

        if (sports.Any(sport => sport == Sports.Baseball))
            franchiseHallOfFameTypes.AddRange(Baseball);

        if (sports.Any(sport => sport == Sports.Basketball))
            franchiseHallOfFameTypes.AddRange(Basketball);

        if (sports.Any(sport => sport == Sports.Football))
            franchiseHallOfFameTypes.AddRange(Football);

        return franchiseHallOfFameTypes.OrderBy(franchiseHallOfFameType => franchiseHallOfFameType.Franchise.Name).ToArray();
    }

    public override string ToString()
        => $"{Franchise.Name} ({Name})";
